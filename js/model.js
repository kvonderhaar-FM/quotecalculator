function getGaugePrice(height, depth, width) {
  if (
    window.gaugeBasePrice[height] &&
    window.gaugeBasePrice[height][depth] &&
    window.gaugeBasePrice[height][depth][width] != null
  ) {
    return window.gaugeBasePrice[height][depth][width];
  }
  return 0; // default if DNE
}

